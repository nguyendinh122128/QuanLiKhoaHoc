    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        // reset arraylist find
        if(txtFind.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Nhập dữ liệu tìm kiếm !");
                
                return;
                
            }
        
        String id_course_find = txtFind.getText();
        String column[] = {"CourseID", "CourseTitle", "Credits", "Department"};
        
        DefaultTableModel model_course_find = new DefaultTableModel();
        model_course_find.setColumnIdentifiers(column);
        
        try {
            courses_find = course_BUS.getCoursesFind(id_course_find);
      
            for(courseDTO course : courses_find){
                model_course_find.addRow(new Object[]{course.getCourseID(),course.getCoureTitle(), course.getCredits()
                    ,course.getDepartment()});
            }
            
            if(courses_find.isEmpty()){
                JOptionPane.showMessageDialog(this, "Không có khóa học có mã như : " + id_course_find);
                
                return;
                
            }else{
                
                tbl_courses.setModel(model_course_find);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnFindActionPerformed

    private void tbl_coursesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_coursesMouseClicked
        // TODO add your handling code here:
        int row = tbl_courses.getSelectedRow();
//        System.out.println("GUI.courses.tbl_coursesMouseClicked() : "+row);
        try {
            if(courses_find.size() > 0) { //Kiểm tra trong arr_courses_find có phần tử
                courseDTO cr = new courseDTO();
                cr = courses_find.get(row);
                lbCourseID.setText(cr.getCourseID()+"");
                txtCredit.setText(cr.getCredits()+"");
                
            }
//            if(course_BUS.getCoureseAll().size() > 0) { //Kiểm tra phần tử trong arr_courses có phần tử // Dòng này bỏ nhưng để cho nhớ
            else{ //Kiểm tra phần tử trong arr_courses có phần tử
                courseDTO cr = new courseDTO();
                cr = course_BUS.getCoureseAll().get(row);
                lbCourseID.setText(cr.getCourseID()+"");
                txtCredit.setText(cr.getCredits()+"");
                
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tbl_coursesMouseClicked

