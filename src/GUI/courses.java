    private void khoiTao_tbl_Course(){
        model_course = new DefaultTableModel();
        String column[] = {"CourseID", "CourseTitle", "Credits", "Department"};
        model_course.setColumnIdentifiers(column);
        try {
            ArrayList<courseDTO> listCourses = course_BUS.getCoureseAll();
            set_TableData_Course(listCourses);
        } catch (Exception e) {
            e.printStackTrace();
        }
        tbl_courses.setModel(model_course);
        
    }
    // Hàm dùng để set dữ liệu thông tin sách vào model của Table Course
    private void set_TableData_Course(ArrayList<courseDTO> listCourse){
        for(courseDTO course : listCourse){
            model_course.addRow(new Object[]{course.getCourseID(),course.getCoureTitle(), course.getCredits()
                    ,course.getDepartment()});
        }
    }
