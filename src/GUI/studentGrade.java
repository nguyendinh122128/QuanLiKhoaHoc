    private void khoiTao_tbl_StudentGrade(){
        //tạo    mới 1 model
        model_studentGrade = new DefaultTableModel();
        
        // Tao cot dau tien
        String column[] = {"EnrollmentID", "CourseID", "StudentID", "Grade"};
        model_studentGrade.setColumnIdentifiers(column);
        
        try {
            ArrayList<studentGradeDTO> listStudentGrades = studentGrade_BUS.getStudentGradesAll();
            //Set du lieu vao table
            set_TableData_StudentGrade(listStudentGrades);
        } catch (Exception e) {
            e.getStackTrace();
//            System.out.println("GUI.studentGrade.khoiTao_tbl_StudentGrade() Error : "+ e.getMessage() + e.getStackTrace());
            JOptionPane.showMessageDialog(null, "GUI.studentGrade.khoiTao_tbl_StudentGrade() Lỗi xảy ra: " + e.getMessage(),
        "Thông báo", JOptionPane.ERROR_MESSAGE); // Hiển thị thông báo lỗi cho người dùng
        }
        
        tblStudenGrade.setModel(model_studentGrade);
        
        
    }
    
    // Hàm dùng để set dữ liệu thông tin sách vào model của Table StudentGrade
    private void set_TableData_StudentGrade(ArrayList<studentGradeDTO> listStudentGrades){
        for(studentGradeDTO studentGrade : listStudentGrades){
            model_studentGrade.addRow(new Object[]{studentGrade.getEnrollmentID(),studentGrade.getCourseID(), studentGrade.getStudenID()
                    ,studentGrade.getGrade()});
        }
    }

