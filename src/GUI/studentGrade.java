    private void tblStudenGradeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudenGradeMouseClicked
        // TODO add your handling code here:
        int row = tblStudenGrade.getSelectedRow();
        try {
                studentGradeDTO studentGrade = new studentGradeDTO();
            if(arr_studentGrades_find.isEmpty()){
                studentGrade = studentGrade_BUS.getStudentGradesAll().get(row);
            }else{
                studentGrade = arr_studentGrades_find.get(row);
            }
                txtEnrollmentID.setText(studentGrade.getEnrollmentID()+"");
                txtGrade.setText(studentGrade.getGrade()+"");
                txtCourseID.setText(studentGrade.getCourseID() + " Title : " + studentGrade_BUS.getTitleCourse(studentGrade.getCourseID()));
                txtStudentID.setText(studentGrade.getStudenID() + " Name : " + studentGrade_BUS.getNameStudent(studentGrade.getStudenID()));
                
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_tblStudenGradeMouseClicked

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        if(txtFind.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Nhập dữ liệu tìm kiếm!");
            return;
        }
        String id_student_find = txtFind.getText();
        String column[] = {"EnrollmentID", "CourseID", "StudentID", "Grade"};
        
        DefaultTableModel model_studentGrade_find = new DefaultTableModel();
        model_studentGrade_find.setColumnIdentifiers(column);
        
        try {
            arr_studentGrades_find = studentGrade_BUS.getStudentGradeFind(id_student_find);
            for(studentGradeDTO studentGrade : arr_studentGrades_find){
                model_studentGrade_find.addRow(new Object[]{studentGrade.getEnrollmentID(),studentGrade.getCourseID(), studentGrade.getStudenID()
                    ,studentGrade.getGrade()});
            }
            
            if(arr_studentGrades_find.isEmpty()){
                JOptionPane.showMessageDialog(this, "Học sinh mã : " + id_student_find + "không có điểm" );
                return;
            }else{
                tblStudenGrade.setModel(model_studentGrade_find);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnFindActionPerformed

